
/**
 * Behavioral Design Patterns Tutorials In software engineering, behavioral
 * design patterns are design patterns that identify common communication
 * patterns between objects and realize these patterns. By doing so, these
 * patterns increase flexibility in carrying out this communication.
 * 
 * Behavioral patterns are concerned with the assignment of responsibilities
 * between objects, or, encapsulating behavior in an object and delegating
 * requests to it.
 * 
 * Unlike the Creational and Structural patterns, which deal with the
 * instantiation process and the blueprint of objects and classes, the central
 * idea here is to concentrate on the way objects are interconnected. In a word,
 * we can say this: If Creational is about instantiation, and Structural is the
 * blueprint, then Behavioral is the pattern of the relationship among objects.
 * 
 * <pre>
	State - Alter an object's behavior when its state changes  
	
	Mediator - Defines simplified communication between classes  
	
	Observer - A way of notifying change to a number of classes  
	
	Memento - Capture and restore an object's internal state  
	
	Iterator  
	
	Interpreter - A way to include language elements in a program
	
	Command - Encapsulate a command request as an object  
	
	Strategy - Encapsulates an algorithm inside a class  
	
	Template - Defer the exact steps of an algorithm to a subclass 
	
	Visitor - Defines a new operation to a class without change 
	
	Chain of Responsibility   - A way of passing a request between a chain of objects
	
 * </pre>
 */
